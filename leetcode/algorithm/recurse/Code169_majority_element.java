	public static int majorityElement(int[] nums) {
		int newNums[] = Arrays.copyOf(nums, nums.length);
		Arrays.sort(newNums);
		return newNums[newNums.length / 2];
	}

	private static Map<Integer, Integer> countNums(int[] nums) {
		Map<Integer, Integer> m = new HashMap<Integer, Integer>();
		for (int i : nums) {
			if (m.containsKey(i)) {
				m.put(i, m.get(i) + 1);
			} else {
				m.put(i, 1);
			}
		}
		return m;
	}

	private static int countInRange(int[] nums, int targer, int lo, int hi) {
		int count = 0;
		for (int i = lo; i <= hi; i++) {
			if (nums[i] == targer) {
				count++;
			}
		}
		return count;
	}

	private static int subMajorElement(int[] nums, int lo, int hi) {
		if (lo == hi) {
			return nums[lo];
		}

		int mid = lo + (hi - lo) / 2;

		int leftRes = subMajorElement(nums, lo, mid);
		int rightRes = subMajorElement(nums, mid + 1, hi);

		if (leftRes == rightRes) {
			return leftRes;
		}

		int leftCount = countInRange(nums, leftRes, lo, hi);
		int rightCount = countInRange(nums, rightRes, lo, hi);
		
		return leftCount > rightCount? leftRes: rightRes;
	}

	// 分治
	public static int majorityElement2(int[] nums) {
		int ans = subMajorElement(nums, 0, nums.length-1);
		return ans;
	}

	public static int majorityElement3(int[] nums) {
		Map<Integer, Integer> m = countNums(nums);
		Map.Entry<Integer, Integer> majorityEntry = null;
		for (Map.Entry<Integer, Integer> entry : m.entrySet()) {
			if (majorityEntry == null || entry.getValue() > majorityEntry.getValue()) {
				majorityEntry = entry;
			}
		}
		return majorityEntry.getKey();
	}

	public static int majorityElement4(int[] nums) {
		int count = 0;
		Integer candidate = null;

		for (int num : nums) {
			if (count == 0) {
				candidate = num;
			}
			count += (num == candidate) ? 1 : -1;
		}

		return candidate;
	}

