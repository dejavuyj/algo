    public static int search(String str, String strRes) {
        int n = 0;//计数器
        int index = 0;//指定字符的长度
        index = str.indexOf(strRes);
        while (index != -1) {
            n++;
            index = str.indexOf(strRes, index + 1);
        }

        return n;
    }

